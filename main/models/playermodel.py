class Player:
    def __init__(self, player_id, name, dob, address, phone, email, url, username, team):
        self.player_id = player_id
        self.name = name
        self.dob = dob
        self.address = address
        self.phone = phone
        self.email = email
        self.url = url
        self.username = username
        self.team = team