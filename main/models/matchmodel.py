class Match:
    def __init__(self, date, time, server_id, team1, team2, score1, score2, winner, round, tournament):
        self.date = date
        self.time = time
        self.server_id = server_id
        self.team1 = team1
        self.team2 = team2
        self.score1 = score1
        self.score2 = score2
        self.winner = winner
        self.round = round
        self.tournament = tournament